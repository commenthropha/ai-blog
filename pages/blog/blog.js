import React from 'react';
import Head from 'next/head';
import moment from 'moment';
import { Navbar, ExtendedPostCard, FeaturePostCard } from '../../components'
import { getPosts, getRecent } from '../../services';
import styles from '../../styles/BlogIndex.module.css';

const BlogIndex = ({ posts, recent }) => {
    return (
        <>
            <Head>
                <title>Create Next App</title>
                <meta name="description" content="Generated by create next app" />
                <meta name="viewport" content="width=device-width, initial-scale=1" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <Navbar />
            <div className={styles.site_blogIndex}>
                <h1>The Blog</h1>
                <div className={styles.site_blogIndex_content}>
                    {recent.map((post, index) => (
                        <FeaturePostCard key={index} post={post} />
                    ))}
                    <div className={styles.site_blogIndex_content_main}>
                        {posts.map((post, index) => (
                            <ExtendedPostCard key={index} post={post} />
                        ))}
                    </div>
                </div>
            </div>
        </>
    )
}

export default BlogIndex;

// Fetch data at build time
export async function getStaticProps() {
    const recent = (await getRecent()) || [];
    const posts = (await getPosts()) || [];

    return {
        props: { recent, posts },
    };
}
